template<typename T_vector>
void output_vector(const T_vector &v, bool add_one = false, int start = -1, int end = -1) {
    if (start < 0) start = 0;
    if (end < 0) end = int(v.size());

    for (int i = start; i < end; i++)
        cout << v[i] + (add_one ? 1 : 0) << (i < end - 1 ? ' ' : '\n');
}

void run_case() {
    int N;
    cin >> N;
    vector<int> B(N - 1);

    for (auto &b : B)
        cin >> b;

    vector<int> A(N, 0);

    for (int i = 0; i < N - 1; i++) {
        A[i] |= B[i];
        A[i + 1] |= B[i];
    }

    for (int i = 0; i < N - 1; i++)
        if ((A[i] & A[i + 1]) != B[i]) {
            cout << -1 << '\n';
            return;
        }

    output_vector(A);
}

int main() {
    ios::sync_with_stdio(false);
#ifndef AJAY_SINGH_DEBUG
    cin.tie(nullptr);
#endif

    int tests;
    cin >> tests;

    while (tests-- > 0)
        run_case();
}
